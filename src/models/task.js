const mongoose = require('mongoose')

//define the Schema for tasks
const taskSchema = new mongoose.Schema({
    description: {
        type: String,
        required: true,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    owner: {
        type: mongoose.Schema.Types.ObjectId,
        required: true,
        ref: 'User'
    }
},{
    timestamps: true
})

//define the model for tasks
const Task = mongoose.model('Task',taskSchema)

module.exports = Task